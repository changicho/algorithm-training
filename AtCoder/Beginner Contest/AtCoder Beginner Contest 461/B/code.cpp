  // freopen("./input.txt", "r", stdin);

  int N;
  cin >> N;

  vector<int> A(N), B(N);
  for (int i = 0; i < N; i++) {
    cin >> A[i];
  }
  for (int i = 0; i < N; i++) {
    cin >> B[i];
  }

  auto answer = solution(N, A, B);

  // cout << answer << endl;
  cout << (answer ? "Yes" : "No") << endl;
